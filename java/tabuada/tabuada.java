public class Main {
    public static void main(String[] args) {
        int parcela = 4;

        System.out.printf("\n==================\n");
        System.out.print("\nAlgoritimo de tabuada\n");

        for (int i = 0; i <= 10; i++) {
            System.out.printf("\n" + parcela + "X" + i + "=" + parcela * i);
        }

        System.out.printf("\n==================\n");
    }
}
